"""Report export system for the Single-Cell Intelligence Agent.

Supports four export formats:
  - export_markdown()      -- structured analysis Markdown report
  - export_json()          -- machine-readable JSON
  - export_pdf()           -- styled PDF via ReportLab Platypus
  - export_fhir_r4()       -- FHIR R4 DiagnosticReport Bundle

Plus specialised report generators:
  - export_cell_type_report()   -- cell type annotation summary
  - export_tme_report()         -- TME profiling report
  - export_drug_response_report() -- drug response prediction report
  - export_anndata_metadata()   -- AnnData-compatible metadata dict

Author: Adam Jones
Date: March 2026
"""

